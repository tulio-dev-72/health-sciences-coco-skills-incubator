        FROM ' || :v_fqn_prefix || '.DOC_TYPE_SPECIFIC_EXTRACTION_CONFIG
        WHERE DOCUMENT_CLASSIFICATION ILIKE DOC_TYPE AND IS_ACTIVE = TRUE
    ''';
END;
$$;

-- NOTE: IDENTIFIER() works for CREATE FUNCTION (unlike CREATE TABLE/SCHEMA/STAGE).
-- We use it here because the Python body contains single quotes that conflict with
-- EXECUTE IMMEDIATE string quoting. IDENTIFIER() avoids that nesting issue.
CREATE OR REPLACE FUNCTION IDENTIFIER($V_DB || '.' || $V_SCHEMA || '.INJECT_IMAGE_DESCRIPTIONS')(PAGE_CONTENT VARCHAR, IMAGE_DATA ARRAY)
RETURNS VARCHAR LANGUAGE PYTHON RUNTIME_VERSION = '3.10' HANDLER = 'inject_descriptions' AS '
def inject_descriptions(page_content, image_data):
    if not page_content or not image_data:
        return page_content
    enhanced_content = page_content
    for img in image_data:
        if not img or "image_id" not in img:
            continue
        image_id = img["image_id"]
        description = img.get("description", "")
        if not description or description == "None":
            continue
        search_pattern = "![" + image_id + "](" + image_id + ")"
        if search_pattern in enhanced_content:
            replacement = search_pattern + chr(10) + chr(10) + "**IMAGE DESCRIPTION:** " + description + chr(10)
            enhanced_content = enhanced_content.replace(search_pattern, replacement, 1)
    return enhanced_content
';

-- =============================================================================
-- STEP 6: DYNAMIC GENERATOR PROCEDURE
-- =============================================================================
-- Reads CLINICAL_DOCS_EXTRACTION_CONFIG (loaded from extraction_config CSV) and:
--   1.  Seeds DOCUMENT_CLASSIFICATION_EXTRACTION_FIELD_CONFIG (CLASSIFICATION rows)
--   1b. Updates classification question with dynamic type list via LISTAGG
--   2.  Seeds DOC_TYPE_SPECIFIC_EXTRACTION_CONFIG (EXTRACTION rows)
--   3.  Generates pivot views dynamically for each doc type (from VIEW_NAME column)
--       Uses IS_IDENTITY_FIELD to resolve identity columns per doc type
--   4.  Generates MRN_PATIENT_MAPPING view
--   5.  Generates the refresh task with dynamic JOINs to all discovered pivot views
--   6.  Generates CLINICAL_DOCS_SEMANTIC_VIEW with DIMENSIONS + METRICS
--   7.  Refreshes DATA_MODEL_KNOWLEDGE.CLINICAL_DOCS_MODEL_REFERENCE from config table (Schema CKE)
--   7b. Refreshes DATA_MODEL_KNOWLEDGE.CLINICAL_DOCS_SPECS_REFERENCE from config table (Spec CKE)
--
-- IMPORTANT — EXECUTION CONSTRAINTS:
--   * This block CANNOT be run via `snow sql -f` (session variables + $$ conflict).
--   * This block CANNOT use nested $$ (Snowflake does not support it).
--   * The proc is FULLY PARAMETERIZED — pass db, schema, warehouse, stage as args:
--     CALL GENERATE_DYNAMIC_OBJECTS('MY_DB', 'MY_SCHEMA', 'MY_WH', 'MY_STAGE')
--     No hardcoded {db}/{schema} placeholders to substitute at creation time.
--   * Cursors use RESULTSET pattern inside BEGIN (EXECUTE IMMEDIATE → RESULTSET
--     → CURSOR FOR rs → FOR rec IN cur) to avoid the DECLARE cursor limitation
--     where variables are not yet available at compile time.
--   * All FOR loops use named cursors from RESULTSETs — inline FOR cursors do NOT
--     support field access by name (e.g. rec.COLUMN_NAME fails silently).
--   * COALESCE always appends ', NULL' to handle single-pivot-view deployments.
--   * Step 7 reads from the config table, NOT INFORMATION_SCHEMA, because views
--     created earlier in the same proc are not visible in INFORMATION_SCHEMA
--     until the transaction commits. DIMENSIONS also use config table for same reason.
--
-- PIVOT COLUMN QUOTING REFERENCE:
--   Snowflake PIVOT produces columns with SINGLE QUOTES in their names.
--   e.g., PIVOT(... FOR FIELD_NAME IN ('MRN', 'PATIENT_NAME'))
--   creates columns literally named 'MRN' and 'PATIENT_NAME' (with quotes).
--   To reference these in a SELECT, use DOUBLE-QUOTED identifiers
--   containing the single quotes:  "'MRN'", "'PATIENT_NAME'"
--   The LISTAGG below must produce:  "'MRN'" AS MRN, "'PATIENT_NAME'" AS PATIENT_NAME
--   Inside this proc (single $$ body, no nesting):
--     Target SQL:   "'MRN'" AS MRN
--     Inside ':     '''' || FIELD_NAME || ''''   (two levels of '' escaping)
-- =============================================================================
-- !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
-- >>> AGENT: DO NOT EXECUTE THIS CREATE PROCEDURE BLOCK.
-- >>> IT WILL FAIL EVERY TIME VIA snowflake_sql_execute. TWO KNOWN BLOCKERS:
-- >>>   1. EXECUTE IMMEDIATE '...' INTO :var  → "unexpected 'INTO'"  (Constraint #16)
-- >>>   2. IDENTIFIER(v_fqn || '...')          → "unexpected 'v_fqn'" (Constraint #17)
-- >>>
-- >>> INSTEAD: Read each numbered sub-step (0 through 7b) below as a TEMPLATE.
-- >>> For each sub-step, write a standalone SQL statement:
-- >>>   - Replace :v_fqn  → '{db}.{schema}'
-- >>>   - Replace :v_db   → '{db}'
-- >>>   - Replace :v_stage_fqn → '{db}.{schema}.{stage}'
-- >>>   - For cursor loops (Step 3, 6, 7), query the config table first to get
-- >>>     the rows, then execute each iteration's SQL individually.
-- >>> Execute each via snowflake_sql_execute — they are plain SQL, no $$ needed.
-- !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
-- =============================================================================
-- TEMPLATE ONLY — The CREATE PROCEDURE below is for Snowsight worksheet
-- execution. CoCo agents must decompose into individual SQL statements.
-- =============================================================================

CREATE OR REPLACE PROCEDURE IDENTIFIER($V_DB || '.' || $V_SCHEMA || '.GENERATE_DYNAMIC_OBJECTS')(P_DB VARCHAR, P_SCHEMA VARCHAR, P_WAREHOUSE VARCHAR, P_STAGE VARCHAR)
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
$$
DECLARE
    v_db VARCHAR;
    v_schema VARCHAR;
    v_warehouse VARCHAR;
    v_stage VARCHAR;
    v_fqn VARCHAR;
    v_stage_fqn VARCHAR;
    v_ddl VARCHAR;
    v_view_name VARCHAR;
    v_doc_type VARCHAR;
    v_field_list VARCHAR;
    v_pivot_in_list VARCHAR;
    v_select_list VARCHAR;
    v_col_def_list VARCHAR;
    v_join_clauses_non_split VARCHAR DEFAULT '';
    v_join_clauses_split VARCHAR DEFAULT '';
    v_coalesce_patient VARCHAR DEFAULT '';
    v_coalesce_mrn VARCHAR DEFAULT '';
    v_view_alias VARCHAR;
    v_alias_counter NUMBER DEFAULT 0;
    v_msg VARCHAR DEFAULT '';
    v_patient_field VARCHAR;
    v_mrn_field VARCHAR;
    v_sv_tables VARCHAR DEFAULT '';
    v_sv_relationships VARCHAR DEFAULT '';
    v_sv_dimensions VARCHAR DEFAULT '';
    v_sv_metrics VARCHAR DEFAULT '';
    v_first_pv VARCHAR DEFAULT '';
    v_dup_count NUMBER DEFAULT 0;
    -- NO cursors in DECLARE — they use RESULTSET pattern in BEGIN
    -- to avoid the limitation where DECLARE cursors cannot reference variables.

BEGIN
    v_db := P_DB;
    v_schema := P_SCHEMA;
    v_warehouse := P_WAREHOUSE;
    v_stage := P_STAGE;
    v_fqn := v_db || '.' || v_schema;
    v_stage_fqn := v_db || '.' || v_schema || '.' || v_stage;

    -- =====================================================================
    -- 0. DEDUPLICATE MASTER CONFIG TABLE
    --    Duplicates arise from repeated CSV loads, re-running Step 1 INSERTs,
    --    or partial re-configurations. Clean before seeding downstream tables.
    -- =====================================================================
    EXECUTE IMMEDIATE '
        SELECT COUNT(*) FROM (
            SELECT CONFIG_TYPE, DOC_TYPE, FIELD_NAME
            FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG
            GROUP BY CONFIG_TYPE, DOC_TYPE, FIELD_NAME
            HAVING COUNT(*) > 1
        )' INTO :v_dup_count;

    IF (v_dup_count > 0) THEN
        EXECUTE IMMEDIATE '
            DELETE FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG
            WHERE ROWID NOT IN (
                SELECT MAX(ROWID)
                FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG
                GROUP BY CONFIG_TYPE, DOC_TYPE, FIELD_NAME
            )';
        v_msg := 'Cleaned ' || v_dup_count || ' duplicate config group(s). ';
    END IF;

    -- =====================================================================
    -- 1. SEED CLASSIFICATION CONFIG from extraction_config (CLASSIFICATION rows)
    -- =====================================================================
    EXECUTE IMMEDIATE '
        DELETE FROM ' || :v_fqn || '.DOCUMENT_CLASSIFICATION_EXTRACTION_FIELD_CONFIG';

    EXECUTE IMMEDIATE '
        INSERT INTO ' || :v_fqn || '.DOCUMENT_CLASSIFICATION_EXTRACTION_FIELD_CONFIG
            (FIELD_NAME, EXTRACTION_QUESTION, TARGET_COLUMN, DATA_TYPE, IS_ACTIVE, DISPLAY_ORDER, CREATED_TIMESTAMP, UPDATED_TIMESTAMP)
        SELECT
            FIELD_NAME,
            EXTRACTION_QUESTION,
            TARGET_COLUMN,
            DATA_TYPE,
            TRUE,
            DISPLAY_ORDER,
            CURRENT_TIMESTAMP(),
            CURRENT_TIMESTAMP()
        FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG
        WHERE CONFIG_TYPE = ''CLASSIFICATION''
        ORDER BY DISPLAY_ORDER';

    v_msg := 'Seeded classification config. ';

    -- =====================================================================
    -- 2. SEED TYPE-SPECIFIC EXTRACTION CONFIG (EXTRACTION rows)
    -- =====================================================================
    EXECUTE IMMEDIATE '
        DELETE FROM ' || :v_fqn || '.DOC_TYPE_SPECIFIC_EXTRACTION_CONFIG';

    EXECUTE IMMEDIATE '
        INSERT INTO ' || :v_fqn || '.DOC_TYPE_SPECIFIC_EXTRACTION_CONFIG
            (DOCUMENT_CLASSIFICATION, FIELD_NAME, EXTRACTION_QUESTION, DATA_TYPE, IS_ACTIVE, DISPLAY_ORDER, CREATED_TIMESTAMP, UPDATED_TIMESTAMP)
        SELECT
            DOC_TYPE,
            FIELD_NAME,
            EXTRACTION_QUESTION,
            DATA_TYPE,
            TRUE,
            DISPLAY_ORDER,
            CURRENT_TIMESTAMP(),
            CURRENT_TIMESTAMP()
        FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG
        WHERE CONFIG_TYPE = ''EXTRACTION''
        ORDER BY DOC_TYPE, DISPLAY_ORDER';

    v_msg := v_msg || 'Seeded type-specific extraction config. ';

    -- =====================================================================
    -- 2b. UPDATE CLASSIFICATION QUESTION with dynamic type list via LISTAGG
    -- =====================================================================
    EXECUTE IMMEDIATE '
        UPDATE ' || :v_fqn || '.DOCUMENT_CLASSIFICATION_EXTRACTION_FIELD_CONFIG
        SET EXTRACTION_QUESTION = ''HOW WOULD YOU CLASSIFY THIS DOCUMENT? CHOOSE STRICTLY ONE VALUE FROM THIS LIST: (''
            || (SELECT LISTAGG(DISTINCT DOC_TYPE, '', '') WITHIN GROUP (ORDER BY DOC_TYPE)
                FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG
                WHERE CONFIG_TYPE = ''EXTRACTION'')
            || '', OTHER)'',
            UPDATED_TIMESTAMP = CURRENT_TIMESTAMP()
        WHERE FIELD_NAME = ''DOCUMENT_CLASSIFICATION''';

    v_msg := v_msg || 'Updated classification question via LISTAGG. ';

    -- =====================================================================
    -- 3. GENERATE PIVOT VIEWS dynamically for each doc type
    --    Uses IS_IDENTITY_FIELD for configurable identity columns
    -- =====================================================================
    LET pivot_rs RESULTSET := (EXECUTE IMMEDIATE
        'SELECT DISTINCT VIEW_NAME, DOC_TYPE FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG WHERE CONFIG_TYPE = ''EXTRACTION'' AND VIEW_NAME IS NOT NULL AND VIEW_NAME != '''' ORDER BY VIEW_NAME');
    LET pivot_cur CURSOR FOR pivot_rs;
    FOR pivot_rec IN pivot_cur DO
        v_view_name := pivot_rec.VIEW_NAME;
        v_doc_type := pivot_rec.DOC_TYPE;
        v_alias_counter := v_alias_counter + 1;
        v_view_alias := 'pv' || v_alias_counter::VARCHAR;

        SELECT
            LISTAGG(FIELD_NAME, ', ') WITHIN GROUP (ORDER BY DISPLAY_ORDER),
            LISTAGG('''' || FIELD_NAME || '''', ', ') WITHIN GROUP (ORDER BY DISPLAY_ORDER),
            LISTAGG(
                CASE WHEN FIELD_NAME = 'PATIENT_NAME' THEN 'UPPER("' || FIELD_NAME || '")' || ' AS ' || FIELD_NAME
                ELSE '"' || FIELD_NAME || '"' || ' AS ' || FIELD_NAME END,
                ', '
            ) WITHIN GROUP (ORDER BY DISPLAY_ORDER)
        INTO :v_field_list, :v_pivot_in_list, :v_select_list
        FROM IDENTIFIER(v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG')
        WHERE CONFIG_TYPE = 'EXTRACTION'
        AND VIEW_NAME = :v_view_name;

        v_col_def_list := 'DOCUMENT_RELATIVE_PATH, DOCUMENT_STAGE, ' || v_field_list;

        v_ddl := '
        CREATE OR REPLACE VIEW ' || v_fqn || '.' || v_view_name || '(' || v_col_def_list || ') AS
        SELECT DOCUMENT_RELATIVE_PATH, DOCUMENT_STAGE, ' || v_select_list || '
        FROM (
            SELECT DOCUMENT_RELATIVE_PATH, DOCUMENT_STAGE, FIELD_NAME, FIELD_VALUE
            FROM ' || v_fqn || '.DOC_TYPE_SPECIFIC_VALUES_EXTRACT_OUTPUT
            WHERE DOCUMENT_CLASSIFICATION = ''' || v_doc_type || '''
        ) PIVOT (MAX(FIELD_VALUE) FOR FIELD_NAME IN (' || v_pivot_in_list || '))';

        EXECUTE IMMEDIATE :v_ddl;

        v_join_clauses_non_split := v_join_clauses_non_split ||
            ' LEFT JOIN ' || v_fqn || '.' || v_view_name || ' ' || v_view_alias ||
            ' ON s.DOCUMENT_RELATIVE_PATH = ' || v_view_alias || '.DOCUMENT_RELATIVE_PATH';

        v_join_clauses_split := v_join_clauses_split ||
            ' LEFT JOIN ' || v_fqn || '.' || v_view_name || ' ' || v_view_alias ||
            ' ON s.PARENT_DOCUMENT_RELATIVE_PATH = ' || v_view_alias || '.DOCUMENT_RELATIVE_PATH';

        SELECT
            COALESCE(MAX(CASE WHEN IS_IDENTITY_FIELD = 'PATIENT_NAME' THEN FIELD_NAME END), 'PATIENT_NAME'),
            COALESCE(MAX(CASE WHEN IS_IDENTITY_FIELD = 'MRN' THEN FIELD_NAME END), 'MRN')
        INTO :v_patient_field, :v_mrn_field
        FROM IDENTIFIER(v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG')
        WHERE CONFIG_TYPE = 'EXTRACTION' AND VIEW_NAME = :v_view_name;

        IF (v_coalesce_patient != '') THEN
            v_coalesce_patient := v_coalesce_patient || ', ';
            v_coalesce_mrn := v_coalesce_mrn || ', ';
        END IF;
        v_coalesce_patient := v_coalesce_patient || v_view_alias || '.' || v_patient_field;
        v_coalesce_mrn := v_coalesce_mrn || v_view_alias || '.' || v_mrn_field;

        v_sv_tables := v_sv_tables ||
            CASE WHEN v_sv_tables != '' THEN ',' ELSE '' END ||
            CHR(10) || '        ' || v_fqn || '.' || v_view_name ||
            CHR(10) || '            COMMENT = ''' || v_doc_type || ' pivot view with extracted fields.'' ';

        v_sv_relationships := v_sv_relationships ||
            CASE WHEN v_sv_relationships != '' THEN ',' ELSE '' END ||
            CHR(10) || '        MRN_TO_' || v_view_name ||
            ' AS ' || v_view_name || '(' || v_mrn_field || ') REFERENCES MRN_PATIENT_MAPPING(MRN)';

        v_msg := v_msg || 'Created view ' || v_view_name || '. ';
    END FOR;

    -- =====================================================================
    -- 4. GENERATE MRN_PATIENT_MAPPING VIEW (simple: from RAW_CONTENT)
    -- =====================================================================
    v_ddl := '
    CREATE OR REPLACE VIEW ' || v_fqn || '.MRN_PATIENT_MAPPING AS
    SELECT DISTINCT MRN, PATIENT_NAME
    FROM ' || v_fqn || '.CLINICAL_DOCUMENTS_RAW_CONTENT
    WHERE MRN IS NOT NULL AND PATIENT_NAME IS NOT NULL';

    EXECUTE IMMEDIATE :v_ddl;
    v_msg := v_msg || 'Created MRN_PATIENT_MAPPING. ';

    -- =====================================================================
    -- 5. GENERATE REFRESH TASK with dynamic JOINs
    --    COALESCE always includes ', NULL' to guarantee 2+ args even with
    --    a single pivot view (prevents Error 8: COALESCE needs 2+ args).
    -- =====================================================================
    v_ddl := '
    CREATE OR REPLACE TASK ' || v_fqn || '.REFRESH_RAW_CONTENT_TASK
        WAREHOUSE = ' || v_warehouse || '
        SCHEDULE = ''99 HOUR''
        COMMENT = ''Incrementally insert new parsed documents and refresh presigned URLs''
        WHEN SYSTEM$STREAM_HAS_DATA(''' || v_fqn || '.DOCS_PARSE_OUTPUT_STREAM'')
    AS
    BEGIN
        CREATE TEMPORARY TABLE IF NOT EXISTS ' || v_fqn || '.TEMP_STREAM_SNAPSHOT AS
        SELECT * FROM ' || v_fqn || '.DOCS_PARSE_OUTPUT_STREAM;

        INSERT INTO ' || v_fqn || '.CLINICAL_DOCUMENTS_RAW_CONTENT (
            DOCUMENT_RELATIVE_PATH, DOCUMENT_STAGE, PAGE_NUMBER_IN_PARENT,
            DOCUMENT_CLASSIFICATION,
            PATIENT_NAME, MRN, PAGE_CONTENT, DOC_TOTAL_PAGES,
            PRESIGNED_URL, STAGE_FILE_URL, URL_GENERATED_AT
        )
        SELECT
            s.DOCUMENT_RELATIVE_PATH, s.DOCUMENT_STAGE, s.PAGE_NUMBER_IN_PARENT,
            dcm_cls.FIELD_VALUE AS DOCUMENT_CLASSIFICATION,
            COALESCE(' || v_coalesce_patient || ', NULL) AS PATIENT_NAME,
            COALESCE(' || v_coalesce_mrn || ', NULL) AS MRN,
            CONCAT(''[Page '', s.PAGE_NUMBER_IN_PARENT, '']'' || CHR(10) || CHR(10), s.PAGE_CONTENT) AS PAGE_CONTENT,
            s.DOC_TOTAL_PAGES,
            GET_PRESIGNED_URL(@' || v_stage_fqn || ', s.DOCUMENT_RELATIVE_PATH, 604800) AS PRESIGNED_URL,
            CONCAT(''snow://stage/'', REPLACE(s.DOCUMENT_STAGE, ''@'', ''''), ''/'', s.DOCUMENT_RELATIVE_PATH) AS STAGE_FILE_URL,
            CURRENT_TIMESTAMP() AS URL_GENERATED_AT
        FROM ' || v_fqn || '.TEMP_STREAM_SNAPSHOT s
        LEFT JOIN ' || v_fqn || '.DOC_CLASSIFICATION_METADATA_ROWS dcm_cls
            ON s.DOCUMENT_RELATIVE_PATH = dcm_cls.DOCUMENT_RELATIVE_PATH
            AND dcm_cls.FIELD_NAME = ''DOCUMENT_CLASSIFICATION'''
        || v_join_clauses_non_split ||
        ' WHERE s.PARENT_DOCUMENT_RELATIVE_PATH IS NULL;

        INSERT INTO ' || v_fqn || '.CLINICAL_DOCUMENTS_RAW_CONTENT (
            DOCUMENT_RELATIVE_PATH, DOCUMENT_STAGE, PAGE_NUMBER_IN_PARENT,
            DOCUMENT_CLASSIFICATION,
            PATIENT_NAME, MRN, PAGE_CONTENT, DOC_TOTAL_PAGES,
            PRESIGNED_URL, STAGE_FILE_URL, URL_GENERATED_AT
        )
        SELECT
            s.PARENT_DOCUMENT_RELATIVE_PATH AS DOCUMENT_RELATIVE_PATH,
            dh.PARENT_DOCUMENT_STAGE AS DOCUMENT_STAGE,
            s.PAGE_NUMBER_IN_PARENT,
            dcm_cls.FIELD_VALUE AS DOCUMENT_CLASSIFICATION,
            COALESCE(' || v_coalesce_patient || ', NULL) AS PATIENT_NAME,
            COALESCE(' || v_coalesce_mrn || ', NULL) AS MRN,
            CONCAT(''[Page '', s.PAGE_NUMBER_IN_PARENT, '']'' || CHR(10) || CHR(10), s.PAGE_CONTENT) AS PAGE_CONTENT,
            s.DOC_TOTAL_PAGES,
            GET_PRESIGNED_URL(@' || v_stage_fqn || ', s.PARENT_DOCUMENT_RELATIVE_PATH, 604800) AS PRESIGNED_URL,
            CONCAT(''snow://stage/'', REPLACE(dh.PARENT_DOCUMENT_STAGE, ''@'', ''''), ''/'', s.PARENT_DOCUMENT_RELATIVE_PATH) AS STAGE_FILE_URL,
            CURRENT_TIMESTAMP() AS URL_GENERATED_AT
        FROM ' || v_fqn || '.TEMP_STREAM_SNAPSHOT s
        JOIN ' || v_fqn || '.DOCUMENT_HIERARCHY dh
            ON s.DOCUMENT_RELATIVE_PATH = dh.DOCUMENT_RELATIVE_PATH AND s.DOCUMENT_STAGE = dh.DOCUMENT_STAGE
        LEFT JOIN ' || v_fqn || '.DOC_CLASSIFICATION_METADATA_ROWS dcm_cls
            ON s.PARENT_DOCUMENT_RELATIVE_PATH = dcm_cls.DOCUMENT_RELATIVE_PATH
            AND dcm_cls.FIELD_NAME = ''DOCUMENT_CLASSIFICATION'''
        || v_join_clauses_split ||
        ' WHERE s.PARENT_DOCUMENT_RELATIVE_PATH IS NOT NULL;

        DROP TABLE IF EXISTS ' || v_fqn || '.TEMP_STREAM_SNAPSHOT;

        UPDATE ' || v_fqn || '.CLINICAL_DOCUMENTS_RAW_CONTENT
        SET PRESIGNED_URL = GET_PRESIGNED_URL(@' || v_stage_fqn || ', DOCUMENT_RELATIVE_PATH, 604800),
            URL_GENERATED_AT = CURRENT_TIMESTAMP()
        WHERE URL_GENERATED_AT < DATEADD(''day'', -6, CURRENT_TIMESTAMP());
    END';

    EXECUTE IMMEDIATE :v_ddl;
    v_msg := v_msg || 'Created REFRESH_RAW_CONTENT_TASK. ';

    -- =====================================================================
    -- 6. GENERATE SEMANTIC VIEW from all discovered pivot views
    --    IMPORTANT: A semantic view REQUIRES at least one DIMENSIONS or
    --    METRICS clause. TABLES + RELATIONSHIPS alone produces:
    --    "No queryable expression is defined in the semantic view."
    --    See: https://docs.snowflake.com/en/sql-reference/sql/create-semantic-view
    --
    --    DIMENSIONS are built from the config table (NOT INFORMATION_SCHEMA)
    --    because views created earlier in this proc are not yet committed.
    --
    --    ⚠️ CRITICAL — DIMENSIONS SYNTAX IS REVERSED FROM NORMAL SQL:
    --       Format:  TABLE_REF.NEW_DIMENSION_NAME AS EXISTING_COLUMN_NAME
    --       The NEW name is on the LEFT of AS, the EXISTING column on the RIGHT.
    --       This is the OPPOSITE of standard SQL (SELECT col AS alias).
    --       Example: DISCHARGE_SUMMARY_V.DS_MRN AS MRN
    --                (DS_MRN = new dimension, MRN = physical column in the view)
    --       WRONG:   TABLE.MRN AS DS_MRN → "invalid identifier" (DS_MRN not a column)
    -- =====================================================================
    IF (v_sv_tables != '') THEN
        -- Build DIMENSIONS from config table (dynamic RESULTSET cursor)
        LET dim_rs RESULTSET := (EXECUTE IMMEDIATE
            'SELECT VIEW_NAME, FIELD_NAME, DOC_TYPE FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG WHERE CONFIG_TYPE = ''EXTRACTION'' ORDER BY VIEW_NAME, DISPLAY_ORDER');
        LET dim_cur CURSOR FOR dim_rs;
        FOR dim_rec IN dim_cur DO
            IF (v_sv_dimensions != '') THEN
                v_sv_dimensions := v_sv_dimensions || ',';
            END IF;
            v_sv_dimensions := v_sv_dimensions ||
                CHR(10) || '        ' || dim_rec.VIEW_NAME || '.' ||
                dim_rec.FIELD_NAME || '_DIM AS ' || dim_rec.FIELD_NAME ||
                CHR(10) || '            COMMENT = ''Extracted ' ||
                REPLACE(dim_rec.FIELD_NAME, '_', ' ') || ' field''';
        END FOR;

        -- Add document path dimensions for each pivot view (dynamic RESULTSET cursor)
        LET pv_dim_rs RESULTSET := (EXECUTE IMMEDIATE
            'SELECT DISTINCT VIEW_NAME FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG WHERE CONFIG_TYPE = ''EXTRACTION'' AND VIEW_NAME IS NOT NULL ORDER BY VIEW_NAME');
        LET pv_dim_cur CURSOR FOR pv_dim_rs;
        FOR pv_rec IN pv_dim_cur DO
            IF (v_sv_dimensions != '') THEN
                v_sv_dimensions := v_sv_dimensions || ',';
            END IF;
            v_sv_dimensions := v_sv_dimensions ||
                CHR(10) || '        ' || pv_rec.VIEW_NAME || '.DOC_PATH AS DOCUMENT_RELATIVE_PATH' ||
                CHR(10) || '            COMMENT = ''Path to source document''';
        END FOR;

        -- Add MRN_PATIENT_MAPPING dimensions
        IF (v_sv_dimensions != '') THEN
            v_sv_dimensions := v_sv_dimensions || ',';
        END IF;
        v_sv_dimensions := v_sv_dimensions ||
            CHR(10) || '        MRN_PATIENT_MAPPING.MRN_KEY AS MRN' ||
            CHR(10) || '            COMMENT = ''Medical record number'',';
        v_sv_dimensions := v_sv_dimensions ||
            CHR(10) || '        MRN_PATIENT_MAPPING.PATIENT_NAME_MAP AS PATIENT_NAME' ||
            CHR(10) || '            COMMENT = ''Patient name from mapping''';

        -- Build METRICS using the first pivot view
        SELECT MIN(VIEW_NAME) INTO :v_first_pv
        FROM IDENTIFIER(v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG')
        WHERE CONFIG_TYPE = 'EXTRACTION' AND VIEW_NAME IS NOT NULL;

        IF (v_first_pv != '') THEN
            v_sv_metrics := '
        ' || v_first_pv || '.PATIENT_COUNT AS COUNT(DISTINCT MRN)
            COMMENT = ''Count of distinct patients'',
        ' || v_first_pv || '.DOCUMENT_COUNT AS COUNT(DISTINCT DOCUMENT_RELATIVE_PATH)
            COMMENT = ''Count of distinct documents''';
        END IF;

        BEGIN
            v_ddl := '
            CREATE OR REPLACE SEMANTIC VIEW ' || v_fqn || '.CLINICAL_DOCS_SEMANTIC_VIEW
                TABLES (' ||
                    v_sv_tables || ',' ||
                    CHR(10) || '        MRN_PATIENT_MAPPING AS ' || v_fqn || '.MRN_PATIENT_MAPPING PRIMARY KEY (MRN)' ||
                    CHR(10) || '            COMMENT = ''Distinct MRN-to-patient-name mapping.''' ||
                CHR(10) || '    )' ||
                CHR(10) || '    RELATIONSHIPS (' ||
                    v_sv_relationships ||
                CHR(10) || '    )' ||
                CHR(10) || '    DIMENSIONS (' ||
                    v_sv_dimensions ||
                CHR(10) || '    )' ||
                CHR(10) || '    METRICS (' ||
                    v_sv_metrics ||
                CHR(10) || '    )' ||
                CHR(10) || '    COMMENT = ''Semantic view for clinical document natural language analytics''';

            EXECUTE IMMEDIATE :v_ddl;
            v_msg := v_msg || 'Created CLINICAL_DOCS_SEMANTIC_VIEW with ' || v_alias_counter || ' pivot views. ';
        EXCEPTION
            WHEN OTHER THEN
                v_msg := v_msg || 'Semantic View creation deferred (pivot views may be empty). Re-run GENERATE_DYNAMIC_OBJECTS() after data is loaded. Error: ' || SQLERRM || '. ';
        END;
    END IF;

    -- =====================================================================
    -- 7. REFRESH SCHEMA CKE (DATA_MODEL_KNOWLEDGE.CLINICAL_DOCS_MODEL_REFERENCE)
    --    Views created earlier in this proc are NOT visible in INFORMATION_SCHEMA
    --    until the transaction commits. The config table is the source of truth.
    --    CLINICAL_DOCS_MODEL_SEARCH_SVC auto-refreshes from this table (TARGET_LAG=1d).
    -- =====================================================================
    BEGIN
        LET v_dm_fqn VARCHAR := v_db || '.DATA_MODEL_KNOWLEDGE';
        EXECUTE IMMEDIATE '
            DELETE FROM ' || :v_dm_fqn || '.CLINICAL_DOCS_MODEL_REFERENCE';

        EXECUTE IMMEDIATE '
            INSERT INTO ' || :v_dm_fqn || '.CLINICAL_DOCS_MODEL_REFERENCE
                (SEARCH_TEXT, TABLE_NAME, COLUMN_NAME, DATA_TYPE, DOMAIN_TAG, CATEGORY, DESCRIPTION, CONSTRAINTS, CONTAINS_PHI, RELATIONSHIPS)
            SELECT
                cfg.DOC_TYPE || '' '' || cfg.FIELD_NAME || '' '' || cfg.EXTRACTION_QUESTION AS SEARCH_TEXT,
                COALESCE(cfg.VIEW_NAME, ''DOC_TYPE_SPECIFIC_VALUES_EXTRACT_OUTPUT'') AS TABLE_NAME,
                cfg.FIELD_NAME AS COLUMN_NAME,
                COALESCE(cfg.DATA_TYPE, ''VARCHAR'') AS DATA_TYPE,
                NULL AS DOMAIN_TAG,
                CASE
                    WHEN cfg.CONFIG_TYPE = ''CLASSIFICATION'' THEN ''Classification''
                    WHEN cfg.VIEW_NAME IS NOT NULL AND cfg.VIEW_NAME != '''' THEN ''Pivot View''
                    ELSE ''Extraction Output''
                END AS CATEGORY,
                cfg.EXTRACTION_QUESTION AS DESCRIPTION,
                '''' AS CONSTRAINTS,
                CASE
                    WHEN cfg.FIELD_NAME IN (''PATIENT_NAME'', ''MRN'') THEN ''YES''
                    WHEN cfg.FIELD_NAME LIKE ''%DIAGNOSIS%'' OR cfg.FIELD_NAME LIKE ''%FINDING%'' THEN ''POTENTIAL''
                    ELSE ''NO''
                END AS CONTAINS_PHI,
                CASE
                    WHEN cfg.IS_IDENTITY_FIELD IN (''MRN'', ''PATIENT_NAME'') THEN ''Identity: '' || cfg.IS_IDENTITY_FIELD
                    ELSE ''''
                END AS RELATIONSHIPS
            FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG cfg
            ORDER BY cfg.CONFIG_TYPE, cfg.DOC_TYPE, cfg.DISPLAY_ORDER';

        v_msg := v_msg || 'Refreshed DATA_MODEL_KNOWLEDGE from config table. ';
    EXCEPTION
        WHEN OTHER THEN
            v_msg := v_msg || 'Model reference refresh skipped (table may not exist yet). ';
    END;

    -- =====================================================================
    -- 7b. REFRESH SPEC CKE (DATA_MODEL_KNOWLEDGE.CLINICAL_DOCS_SPECS_REFERENCE)
    --     Populates the Spec CKE table from the same config table.
    --     Enables dynamic discovery: "What fields does a discharge summary have?"
    --     CLINICAL_DOCS_SPECS_SEARCH_SVC auto-refreshes from this table (TARGET_LAG=1d).
    --     Source of truth: references/document_type_specs.yaml -> config table -> here
    -- =====================================================================
    BEGIN
        LET v_spec_fqn VARCHAR := v_db || '.DATA_MODEL_KNOWLEDGE';
        EXECUTE IMMEDIATE '
            DELETE FROM ' || :v_spec_fqn || '.CLINICAL_DOCS_SPECS_REFERENCE';

        EXECUTE IMMEDIATE '
            INSERT INTO ' || :v_spec_fqn || '.CLINICAL_DOCS_SPECS_REFERENCE
                (SEARCH_TEXT, DOC_TYPE, FIELD_NAME, EXTRACTION_QUESTION, DATA_TYPE,
                 DISPLAY_ORDER, VIEW_NAME, IS_IDENTITY_FIELD, CONTAINS_PHI, DESCRIPTION)
            SELECT
                cfg.DOC_TYPE || '' '' || cfg.FIELD_NAME || '' '' || cfg.EXTRACTION_QUESTION AS SEARCH_TEXT,
                cfg.DOC_TYPE,
                cfg.FIELD_NAME,
                cfg.EXTRACTION_QUESTION,
                COALESCE(cfg.DATA_TYPE, ''VARCHAR'') AS DATA_TYPE,
                cfg.DISPLAY_ORDER,
                cfg.VIEW_NAME,
                cfg.IS_IDENTITY_FIELD,
                CASE
                    WHEN cfg.FIELD_NAME IN (''PATIENT_NAME'', ''MRN'') THEN ''YES''
                    WHEN cfg.FIELD_NAME LIKE ''%DIAGNOSIS%'' OR cfg.FIELD_NAME LIKE ''%FINDING%'' THEN ''POTENTIAL''
                    ELSE ''NO''
                END AS CONTAINS_PHI,
                cfg.EXTRACTION_QUESTION AS DESCRIPTION
            FROM ' || :v_fqn || '.CLINICAL_DOCS_EXTRACTION_CONFIG cfg
            WHERE cfg.CONFIG_TYPE = ''EXTRACTION''
            ORDER BY cfg.DOC_TYPE, cfg.DISPLAY_ORDER';

        v_msg := v_msg || 'Refreshed Spec CKE (CLINICAL_DOCS_SPECS_REFERENCE). ';
    EXCEPTION
        WHEN OTHER THEN
            v_msg := v_msg || 'Spec CKE refresh skipped (table may not exist yet). ';
    END;

    RETURN v_msg;
END;
$$;

