-- =============================================================================
-- CLINICAL DOCUMENTS AI-POWERED EXTRACTION - DYNAMIC PIPELINE SETUP
-- =============================================================================
-- AUTHORITATIVE SOURCE: references/document_type_specs.yaml
--   This YAML spec file defines doc types, extraction fields, prompts, and PHI
--   flags. The config table (CLINICAL_DOCS_EXTRACTION_CONFIG) is DERIVED from
--   these specs — not the other way around.
--
-- Two CKE (Cortex Knowledge Extension) layers feed the skill at runtime:
--   1. Schema CKE  — CLINICAL_DOCS_MODEL_SEARCH_SVC (auto-generated by Step 7)
--      Answers: "What tables exist?" "Which columns contain PHI?"
--   2. Spec CKE    — CLINICAL_DOCS_SPECS_SEARCH_SVC (auto-generated by Step 7b)
--      Answers: "What fields does a discharge summary have?"
--
-- Flow: document_type_specs.yaml -> extraction_config CSV / direct INSERT ->
--       config table -> GENERATE_DYNAMIC_OBJECTS() ->
--       seeds classification/extraction configs, updates classification question
--       via LISTAGG, generates pivot views, refresh task, Semantic View,
--       refreshes Schema CKE corpus, and refreshes Spec CKE corpus.
--
-- Adding a new doc type:
--   1. Add entry to references/document_type_specs.yaml (authoritative spec)
--   2. Seed config table from spec (INSERT rows or load CSV)
--   3. CALL GENERATE_DYNAMIC_OBJECTS() — one call does everything
-- See references/metadata_as_cke.md for the full CKE-driven pattern.
-- =============================================================================
--
-- !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
-- CLI INCOMPATIBILITY WARNING
-- !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
-- This script is designed for Snowsight worksheet execution (single session).
-- It WILL NOT work with `snow sql -f` due to:
--   1. Session variables ($V_DB, $V_SCHEMA) are lost between semicolons
--   2. EXECUTE IMMEDIATE with || concatenation gets split at wrong boundaries
--   3. Nested $$ delimiters in stored procedure bodies cause parse errors
--   4. Shell interprets $V_DB as a shell variable (empty) in snow sql -q
--
-- FOR CLI / CoCo EXECUTION:
--   1. Replace all $V_DB, $V_SCHEMA, $V_WAREHOUSE with hardcoded values
--   2. Write each STEP section to a separate temp file
--   3. Execute via: snow sql -c {connection} -f /tmp/step_N.sql
--   4. For GENERATE_DYNAMIC_OBJECTS proc, hardcode v_fqn in the proc body
--
-- The snowflake_sql_execute CoCo tool supports the connection parameter
-- for targeting non-default connections.
-- !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
--
-- EXECUTION NOTES (for CoCo agents executing this script):
-- -------------------------------------------------------
-- 1. IDENTIFIER() LIMITATION: Snowflake's IDENTIFIER() function does NOT support
--    expression concatenation (|| operator) inside DDL statements like
--    CREATE SCHEMA, CREATE STAGE, CREATE TABLE. It only works with single
--    session variables (e.g., IDENTIFIER($V_DB)) or simple string literals.
